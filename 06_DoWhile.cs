namespace FundamentosDoCsharp
{
    public class DoWhile
    {
        public static void DoWhileEx()
        {
            //---  ---  Laço de repetição: DO WHILE  ---  ---

            var valor = 0;
            do
            {
                Console.WriteLine("Teste");
                valor++;
            } while (valor < 5);
        }
    }
}